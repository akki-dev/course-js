/** Colocamos Var o Let para decir
 * que estamos creando una variable */

var name = "Jon";
let lastname = "Martinez";

//Constante
/** Constante hace que no se pueda cambiar, no puede tener otro valor */
const nick = 'Killer';
/**Si no colocamos la constante puede variar*/
lastname = 'Julio';

console.log(name)
console.log(lastname)
console.log(nick)

